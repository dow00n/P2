package mahasiswa;
public class mahasiswa {
       String nama;
       int nilai_mahasiswa;

       //dengan parameter
       mahasiswa(String nama, int nilai_mahasiswa){
           this.nama = nama;
           this.nilai_mahasiswa = nilai_mahasiswa;
       }
       
        //tanpa parameter
        mahasiswa(){
        nama = "Muhammad Rafli";
        nilai_mahasiswa = 85;
       }
        
public static void main(String[] args){
    mahasiswa mahasiswa1 = new mahasiswa();
    mahasiswa mahasiswa2 = new mahasiswa("Rama" , 85);
    System.out.println("nama  " + mahasiswa1.nama);
    System.out.println("nilai_mahasiswa" + mahasiswa1.nilai_mahasiswa);
    
    System.out.println("nama  " + mahasiswa2.nama);
    System.out.println("nilai_mahasiswa" + mahasiswa2.nilai_mahasiswa);
}
}